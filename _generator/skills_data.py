# Keys per skill:
#   t  title
#   d  one-line description
#   p  purpose paragraph
#   f  focus areas (list)
#   dl deliverables (list)
#   ap anti-patterns (list)
#   tpl templates: list of (slug, title)
#   m  success metrics (optional list)

