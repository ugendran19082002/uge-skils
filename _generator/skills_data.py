def build_skills():
    skills = []
    for cat_slug, entries in R.items():
        for slug, e in entries.items():
            skills.append({
                "slug": slug,
                "category_slug": cat_slug,
                "category_title": CATEGORIES[cat_slug],
                "title": e["t"],
                "desc": e["d"],
                "purpose": e["p"],
                "focus": e["f"],
                "deliverables": e["dl"],
                "antipatterns": e["ap"],
                "templates": e["tpl"],
                "metrics": e["m"],
                "tags": [],
            })
    return skills


